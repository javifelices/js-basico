var frutas = ["Manzana", "Plátano", "Cereza", "Fresa"];
frutas.push("Uvas"); // Inserta un elemento al final. Devuelve un número
console.log(frutas);
frutas.pop(); // Elimina el último elemento. Devuelve un string
console.log(frutas);
frutas.unshift("Uvas"); // Inserta un elemento al principio. Devuelve un número
console.log(frutas);
frutas.shift(); // Elimina el primer elemento. Devuelve un string
console.log(frutas);
frutas.indexOf("Cereza"); // Devuelve un número que es la posición del elemento.
